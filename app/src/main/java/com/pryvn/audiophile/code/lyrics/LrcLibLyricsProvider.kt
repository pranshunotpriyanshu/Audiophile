package com.pryvn.audiophile.code.lyrics

import moe.rukamori.archivetune.lrclib.LrcLib

object LrcLibLyricsProvider : LyricsProvider {
    override val name: String = "LrcLib"

    override suspend fun getLyrics(
        id: String,
        title: String,
        artist: String,
        album: String?,
        durationSeconds: Int,
    ): Result<String> = LrcLib.getLyrics(title, artist, durationSeconds, album)
}
